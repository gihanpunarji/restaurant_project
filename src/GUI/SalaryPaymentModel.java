    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:

        if (evt.getClickCount() > 1) {

        } else {
            if (jTextField4.getText() == "") {
                JOptionPane.showMessageDialog(this, "Please enter Additional Payment ");

            } else {
                String additional = jTextField4.getText();
                String netPay = jTextField5.getText();

                try {

                    MYSQL.iud("UPDATE `salary` SET `additional`='" + additional + "',`net_pay`='" + netPay + "',`salary_status`= '1' WHERE `employee_id`= '" + employeeId + "' ");
                    if (refresh != null) {
                        refresh.run();
                        this.dispose();
                    }

                } catch (Exception e) {
                    e.printStackTrace();
                }

            }
        }

    }//GEN-LAST:event_jButton1MouseClicked

