    private void jTextField4KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField4KeyReleased
        // TODO add your handling code here:

        double besic = Double.parseDouble(jTextField2.getText());

        if (jTextField4.getText().equals("")) {
            jTextField5.setText(String.valueOf(besic));

        } else {
            double additional = Double.parseDouble(jTextField4.getText());
            jTextField5.setText(String.valueOf(besic + additional));
        }

    }//GEN-LAST:event_jTextField4KeyReleased

