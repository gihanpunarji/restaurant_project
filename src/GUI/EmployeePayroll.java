    private void updateTime() {
        Date now = new Date();

        SimpleDateFormat time = new SimpleDateFormat("HH:mm:ss");
        String formattedTime = time.format(now);

        SimpleDateFormat dateFormat = new SimpleDateFormat(" dd-MMM-yyyy");
        String formattedDate = dateFormat.format(now);

        SimpleDateFormat dayFormat = new SimpleDateFormat("EEEE");
        String formattedDay = dayFormat.format(now);

        jLabel1.setText(formattedDate);
        jLabel4.setText(formattedDay);
       

    }

    private void displayTimeAndDate() {
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd-MM-yyyy");
        Date date = new Date();

        LocalDate day = LocalDate.now();

        jLabel3.setText(day.getDayOfWeek().getDisplayName(TextStyle.FULL, Locale.ENGLISH));
        jLabel2.setText(String.valueOf(dateFormat.format(date)));
    }

    private void LiveClock() {

        Timer timer = new Timer(1000, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                updateTime();
            }
        });
        timer.start();
    }

