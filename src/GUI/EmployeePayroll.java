    public void loadEmployee(String query) {

        try {

            ResultSet search = MYSQL.search("SELECT * FROM salary INNER JOIN employee ON salary.employee_id = employee.employee_id" + query);
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

            JTableHeader header = new JTableHeader();
            header.setBackground(new Color(186, 112, 79));
            header.setForeground(new Color(255, 255, 255));

            model.setRowCount(0);

            DefaultTableCellRenderer render = new DefaultTableCellRenderer();
            render.setHorizontalAlignment(SwingConstants.CENTER);
            jTable1.setDefaultRenderer(Object.class, render);

            while (search.next()) {
                Vector v = new Vector();
                v.add(search.getString("employee_id"));
                v.add(search.getString("mobile"));
                v.add(search.getString("fname") + " " + search.getString("lname"));
                v.add(search.getString("pay_besic"));
                v.add(search.getString("additional"));
                v.add(search.getString("net_pay"));

                model.addRow(v);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

